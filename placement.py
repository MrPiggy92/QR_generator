def place(dataBits):
    #print(dataBits)
    #print(len(dataBits))
    n = 37  # Size of Version 5 QR code matrix
    matrix = initializeMatrix(n)  # Initialize a 37x37 matrix with 0 or placeholders
    row = n - 1  # Start at the bottom-right corner
    column = n - 1  # Start at the rightmost column
    direction = -1  # Movement direction (-1 for up, +1 for down)
    bitIndex = 0  # Tracks the current bit in dataBits
    dataBits = dataBits[::-1]

    while bitIndex < len(dataBits):
        # Skip the timing patterns column
        if column == 6:
            column -= 1

        # Process two columns at a time, right to left
        for i in [0, 1]:
            currentCol = column - i

            if not isReserved(row, currentCol, n):
                if currentCol < 0:
                    bitIndex = 9999999
                    break
                # Place the bit in the matrix
                matrix[row][currentCol] = int(dataBits[bitIndex])
                #print(f"({row}, {currentCol}) {dataBits[bitIndex]}")
                #input()
                bitIndex += 1
                #print(bitIndex)
                if bitIndex == len(dataBits):
                    break

        # Move to the next row in the current direction
        row += direction

        # If we reach the top or bottom, reverse direction and move left
        if row < 0 or row >= n:
            row -= direction  # Step back to stay in bounds
            direction *= -1  # Reverse direction
            column -= 2  # Move left two columns

    # Fill remaining spaces with padding bits if needed
    #fillEmptySpaces(matrix)
    
    #for row in matrix:
    #    print(' '.join([str(bit) for bit in row]))
    return matrix

def isReserved(row, column, n):
    if layout[row][column] == -1:
        return True
    return False

